static mrb_value
mrb_scintilla_create_lexer(mrb_state *mrb, mrb_value self)
{
  char *lang = NULL;
  mrb_get_args(mrb, "z", &lang);

  ILexer5 *pLexer = CreateLexer(lang);

  return mrb_cptr_value(mrb, pLexer);
}

